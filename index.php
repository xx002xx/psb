         <div class="row">
            <div class="col-md-12">
               <h3>Sejarah Yayasan</h3>
               <p style="text-align: justify;">Yayasan Pendidikan Karya Bhakti (YPKB) didirikan pada tahun 2003 oleh
                  Dede Apandi dan rekannya, dengan tujuan untuk memperjuangkan hak-hak kewarganegaraan anak-anak
                  disabilitas yang belum terakomodasi penuh oleh sistem pendidikan pada saat itu. YPKB berkomitmen untuk
                  memberikan pendidikan formal secara gratis kepada anak-anak disabilitas dari jenjang SD, SMP, dan SMA
                  melalui Sekolah Luar Biasa B Karya Bhakti yang didirikan pada awal berdirinya yayasan. Selain program
                  pendidikan formal, YPKB juga memiliki program pendidikan non-formal, seperti pelatihan vokasional dan
                  pendampingan usaha mikro kecil dan menengah (UMKM) bagi masyarakat disabilitas.</p>
               <p style="text-align: justify;">YPKB berusaha untuk menciptakan lingkungan inklusif bagi anak-anak
                  disabilitas dan masyarakat secara umum, sehingga mereka dapat berkembang secara pribadi, sosial, dan
                  finansial. Setelah 20 tahun berdirinya, YPKB terus berupaya untuk beradaptasi, berinovasi, dan
                  memberikan manfaat yang lebih luas bagi masyarakat secara inklusif. Oleh karena itu, pada tahun 2023,
                  YPKB mengamanatkan kepemimpinan yayasan kepada Saeful Hikmah Apandi untuk melakukan restrukturisasi
                  dan reorientasi agar yayasan dapat berfungsi secara lebih efektif dan bermanfaat bagi masyarakat.</p>
               <h3>Filosofi Yayasan</h3>
               <p style="text-align: justify;">Yayasan Pendidikan Karya Bhakti telah berusia 20 tahun, halangan dan
                  cobaan telah banyak dialami oleh kepengurusan yayasan sebelumnya. Dengan semangat dan spirit yang sama
                  seperti awal dibentuknya yayasan, kepengurusan tahun 2023-2028 akan tetap membawa nilai-nilai
                  kemanusiaan yang adil dan beradab untuk masyarakat disabilitas. Itikad ini akan kami wujudkan dengan
                  cara yang lebih adaptif dengan zaman dan inovatif yang komprehensif agar yayasan ini memiliki asas
                  kebermanfaatan untuk anak disabilitas, orang tua yang memiliki anak disabilitas, dan masyarakat umum
                  yang membutuhkan pendampingan secara inklusif.</p>
               <h3>Profil Pendiri Yayasan</h3>
               <p style="text-align: justify;">Dede Apandi, S.Pd, adalah seorang pendidik yang memiliki keahlian di
                  bidang Pendidikan Luar Biasa yang didapatkannya dari Universitas Pendidikan Indonesia. Ia kemudian
                  memanfaatkan keahliannya tersebut untuk membantu dunia disabilitas di Indonesia. Pada periode
                  1998-2008, ia menjabat sebagai Sekretaris National Paralympic Committee Indonesia (NPCI), yang
                  sebelumnya dikenal sebagai Yayasan Pembina Olahraga Cacat (YPOC). Selanjutnya, ia berpindah posisi
                  menjadi Ketua Bidang Pemberdayaan Atlit pada tahun 2008 dan menjabat hingga sekarang.</p>
               <p style="text-align: justify;">Dede Apandi juga memiliki peran aktif dalam organisasi penyandang
                  disabilitas, yaitu Perkumpulan Penyandang Disabilitas Indonesia (PPDI) Provinsi Jawa Barat, dimana ia
                  menjabat sebagai Bendahara Umum. Pada tahun 2003, Dede Apandi mendirikan Yayasan Pendidikan Karya
                  Bhakti dengan tujuan untuk memberikan akses pendidikan yang lebih baik dan gratis bagi anak-anak
                  disabilitas di Indonesia. Dalam kurun waktu 20 tahun sejak berdiri, Yayasan Pendidikan Karya Bhakti
                  telah memberikan banyak kontribusi positif bagi masyarakat disabilitas, terutama melalui program
                  sekolah luar biasa gratis yang mereka sediakan.</p>
